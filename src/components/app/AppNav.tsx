export function AppNav() {
  const pathname = usePathname();

  function isActive(href: string) {
    if (!pathname) return false;
    // Strip locale prefix
    const path = pathname.replace(/^\/(tr|de|es|ar|zh)/, "") || "/";
    if (href === "/groups") return path.startsWith("/groups");
    return path === href;
  }

  return (
    <nav className="flex items-center gap-1 p-1 rounded-full bg-surface-2 border border-outline">
      {links.map((link) => {
        const active = isActive(link.href);
        return (
          <a
            key={link.href}
            href={link.href}
            className={`inline-flex items-center gap-1.5 px-3 py-1.5 rounded-full text-xs font-medium transition-all ${
              active
                ? "bg-surface-1 text-primary shadow-sm"
                : "text-text-muted hover:text-text"
            }`}
            style={{ transitionDuration: "var(--dur-1)" }}
          >
            {link.icon}
            <span className="hidden md:inline">{link.label}</span>
          </a>
        );
      })}
    </nav>
  );
}
